/**
 * _add - addition
 * @a: number
 * @b: nuber
 * Return: result
 */

int _add(int a, int b)
{
	return (a + b);
}

/**
 * _sub - substration
 * @a: number
 * @b: nuber
 * Return: result
 */

int _sub(int a, int b)
{
	return (a - b);
}

/**
 * _mul - multiplication
 * @a: number
 * @b: number
 * Return: result
 */

int _mul(int a, int b)
{
	return (a * b);
}

/**
 * div - division
 * @a: number
 * @b: number
 * Return: result
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - modulus
 * @a: number
 * @b: number
 * Return: result
 */

int mod(int a, int b)
{
	return (a % b);
}
